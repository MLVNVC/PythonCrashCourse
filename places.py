places = ['Switzerland', 'Norway', 'USA', 'Brasil', 'Japan']
print(places)
print(sorted(places)) #temporarily alph.order
print(places)
print(sorted(places, reverse=True)) #temporarily reversed alph.order
places.reverse() # permanently reversed, not alph.order, just from right to left
print(places)
places.reverse() # you can reversed with reverse 
print(places)
places.sort() # permanently alph.order
print(places)
places.sort(reverse=True)
print(places)
